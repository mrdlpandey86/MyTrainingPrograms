package com.flowcontrol;

public class Switch {

	public static void main(String[] args) {
		char ch = 'A';

		switch (ch) {
		case 'A':
			System.out.println("A Grade");
			break;
		case 'B':
			System.out.println("B Grade");
			break;
		case 'C':
			System.out.println("C grade");
			break;
		default:
			System.out.println("D Grade");

		}

	}

}
